#!/bin/bash

# Скрипт для сборки и запуска приложения

echo "🔨 Сборка проекта..."

# Проверяем, не была ли директория создана с правами root
if [ -d "build" ] && [ ! -w "build" ]; then
    echo "⚠️  Директория build имеет неправильные права доступа."
    echo "💡 Попробуйте удалить её вручную: sudo rm -rf build"
    exit 1
fi

# Удаляем старую директорию сборки, если она существует
if [ -d "build" ]; then
    echo "🧹 Очистка старой директории сборки..."
    rm -rf build
fi

# Создаем новую директорию для сборки
mkdir -p build
cd build

# Запускаем CMake
echo "📦 Настройка CMake..."
cmake .. || {
    echo "❌ Ошибка при настройке CMake"
    exit 1
}

# Собираем проект
echo "🔧 Компиляция..."
make || {
    echo "❌ Ошибка при компиляции"
    exit 1
}

echo "✅ Сборка завершена успешно!"
echo ""
echo "🚀 Запуск сервера..."
echo "📱 Откройте браузер и перейдите по адресу: http://localhost:8080"
echo ""

# Запускаем приложение
./DutyScheduleApp

